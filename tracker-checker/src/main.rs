fn parse_args() -> Result<(String, String, usize), String> {
    let mut args = std::env::args().skip(1).collect::<Vec<_>>();
    if args.is_empty() {
        return Ok((
            "trackers.txt".to_string(),
            "trackers_best.txt".to_string(),
            DEFAULT_WORKERS,
        ));
    }

    let mut workers = DEFAULT_WORKERS;
    if let Some(pos) = args.iter().position(|a| a == "--workers") {
        if pos + 1 >= args.len() {
            return Err("--workers requires a value".to_string());
        }
        workers = args[pos + 1]
            .parse::<usize>()
            .map_err(|_| "Invalid --workers value".to_string())?;
        args.drain(pos..=pos + 1);
    }

    let input = args
        .first()
        .cloned()
        .unwrap_or_else(|| "trackers.txt".to_string());
    let output = args
        .get(1)
        .cloned()
        .unwrap_or_else(|| "trackers_best.txt".to_string());

    Ok((input, output, workers.max(1)))
}

fn status_tag(status: &Status) -> &'static str {
    match status {
        Status::Alive => "ALIVE",
        Status::Dead => "DEAD",
        Status::Invalid => "INVALID",
    }
}

fn main() {
    let (input_file, output_file, workers) = match parse_args() {
        Ok(v) => v,
        Err(e) => {
            eprintln!("❌ {e}");
            std::process::exit(1);
        }
    };

    println!("======================================================================");
    println!("TRACKER CHECKER - RUST STRICT MODE");
    println!("======================================================================");
    println!("Input file: {input_file}");
    println!("Output file: {output_file}");
    println!("Workers: {workers}");
    println!("======================================================================\n");

    let trackers = match load_trackers(&input_file) {
        Ok(v) => v,
        Err(e) => {
            eprintln!("{e}");
            std::process::exit(1);
        }
    };

    let trackers = filter_blacklist(trackers, "blackstr.txt");
    let trackers = sort_and_dedupe(trackers);
    if trackers.is_empty() {
        eprintln!("Error: No trackers found in {input_file}");
        std::process::exit(1);
    }

    if let Err(e) = write_lines(PathBuf::from(&input_file), &trackers) {
        eprintln!("❌ {e}");
        std::process::exit(1);
    }

    println!("Checking {} trackers with STRICT validation...", trackers.len());

    let timeout = Duration::from_secs(DEFAULT_TIMEOUT_SECS);
    let total = trackers.len();
    let trackers_for_workers = trackers.clone();
    let (tx, rx) = mpsc::channel::<CheckResult>();

    let worker_handle = thread::spawn(move || {
        let pool = rayon::ThreadPoolBuilder::new()
            .num_threads(workers)
            .build()
            .expect("failed to create thread pool");

        pool.install(|| {
            trackers_for_workers
                .par_iter()
                .for_each_with(tx, |sender, tracker| {
                    let result = validate_tracker(tracker, timeout);
                    let _ = sender.send(result);
                });
        });
    });

    let mut all_results: Vec<CheckResult> = Vec::with_capacity(total);
    let mut alive: Vec<CheckResult> = Vec::new();
    let mut dead_count = 0usize;
    let mut invalid_count = 0usize;
    let mut completed = 0usize;

    for result in rx {
        completed += 1;
        println!(
            "[{}/{}] {} {}",
            completed,
            total,
            status_tag(&result.status),
            result.url
        );

        all_results.push(result.clone());

        match result.status {
            Status::Alive => alive.push(result),
            Status::Dead => dead_count += 1,
            Status::Invalid => invalid_count += 1,
        }
    }

    if worker_handle.join().is_err() {
        eprintln!("❌ checker worker thread panicked");
        std::process::exit(1);
    }

    println!("Generating trackers_best.txt...");
    if let Err(e) = write_best_file(&alive, &output_file) {
        eprintln!("❌ {e}");
        std::process::exit(1);
    }

    println!("Splitting trackers_best.txt by protocol...");
    if let Err(e) = split_best_file_by_protocol(&output_file) {
        eprintln!("❌ {e}");
        std::process::exit(1);
    }

    println!("Generating GitHub Pages static files...");
    if all_results.len() != total {
        eprintln!("❌ internal result count mismatch");
        std::process::exit(1);
    }

    if let Err(e) = generate_github_pages(&all_results, &input_file, &output_file) {
        eprintln!("❌ {e}");
        std::process::exit(1);
    }

    println!("\nDone! Results saved to {output_file}");
    println!("✓ Alive (Valid Trackers): {}", alive.len());
    println!("✗ Dead: {dead_count}");
    println!("⚠ Invalid (Parked/Expired/Not Trackers): {invalid_count}");
    println!("🌐 GitHub Pages files: docs/index.html, docs/public-stats/index.html");
}
