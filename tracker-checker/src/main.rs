#[derive(Debug, Clone, PartialEq, Eq)]
enum Status {
    Alive,
    Dead,
    Invalid,
}

#[derive(Debug, Clone)]
struct CheckResult {
    url: String,
    status: Status,
}

fn parse_tracker_line(line: &str) -> String {
    let trimmed = line.trim();
    if trimmed.starts_with('[') && trimmed.contains("](") && trimmed.ends_with(')') {
        if let Some((_, right)) = trimmed.split_once("](") {
            return right.trim_end_matches(')').to_string();
        }
    }
    trimmed.to_string()
}

fn tracker_regex() -> &'static Regex {
    static RE: OnceLock<Regex> = OnceLock::new();
    RE.get_or_init(|| Regex::new(r"(?i)(https?|udp|wss)://[^\s,]*?/announce")
        .expect("invalid tracker regex"))
}

fn collapse_path_slashes(path: &str) -> String {
    let mut out = String::with_capacity(path.len());
    let mut prev_slash = false;

    for ch in path.chars() {
        if ch == '/' {
            if !prev_slash {
                out.push(ch);
            }
            prev_slash = true;
        } else {
            out.push(ch);
            prev_slash = false;
        }
    }

    if out.is_empty() {
        "/".to_string()
    } else {
        out
    }
}

fn normalize_tracker_url(raw: &str) -> Option<String> {
    let candidate = raw
        .trim()
        .trim_matches(|c: char| "\"'<>[](){};,.".contains(c));

    let mut url = Url::parse(candidate).ok()?;
    let scheme = url.scheme().to_lowercase();

    if !matches!(scheme.as_str(), "http" | "https" | "udp" | "wss") {
        return None;
    }

    let host = url.host_str()?;
    let host_is_ip = host.parse::<IpAddr>().is_ok();
    if !host_is_ip && !host.contains('.') {
        return None;
    }

    let normalized_path = collapse_path_slashes(url.path());
    if !normalized_path.ends_with("/announce") {
        return None;
    }
    url.set_path(&normalized_path);
    url.set_query(None);
    url.set_fragment(None);

    let port = url.port();
    let should_drop_default_port = (scheme == "http" && port == Some(80))
        || (scheme == "https" && port == Some(443));

    if should_drop_default_port {
        let _ = url.set_port(None);
    }

    Some(url.to_string())
}

fn bdecode_simple(data: &[u8]) -> bool {
    if data.is_empty() || data[0] != b'd' {
        return false;
    }

    data.windows(b"interval".len()).any(|w| w == b"interval")
        || data.windows(b"peers".len()).any(|w| w == b"peers")
        || data
            .windows(b"failure reason".len())
            .any(|w| w == b"failure reason")
}

fn is_parked_or_expired_domain(content: &[u8]) -> bool {
    let content_lower = String::from_utf8_lossy(content).to_lowercase();
    let indicators = [
        "domain for sale",
        "domain is for sale",
        "buy this domain",
        "domain expired",
        "domain has expired",
        "this domain is expired",
        "register this domain",
        "domain available",
        "parked domain",
        "domain parking",
        "coming soon",
        "under construction",
        "page not found",
        "404 not found",
        "namecheap",
        "godaddy parked",
        "sedo domain parking",
        "domain portfolio",
        "premium domain",
        "afternic",
        "escrow.com",
        "dan.com/buy-domain",
        "undeveloped",
        "this webpage was generated by the domain owner",
        "hugedomains.com",
        "bodis.com",
        "sedoparking",
    ];

    if indicators.iter().any(|word| content_lower.contains(word)) {
        return true;
    }

    let head = String::from_utf8_lossy(&content[..content.len().min(500)]).to_lowercase();
    head.contains("<html") || head.contains("<!doctype html")
}

fn check_http_tracker(url: &str, timeout: Duration) -> Status {
    let client = match Client::builder().timeout(timeout).build() {
        Ok(c) => c,
        Err(_) => return Status::Dead,
    };

    let mut rng = rand::thread_rng();
    let peer_id_random: String = (0..12)
        .map(|_| (rng.gen_range(0u8..=9) + b'0') as char)
        .collect();
    let peer_id = format!("-RS0001-{peer_id_random}");

    let response = match client
        .get(url)
        .query(&[
            ("info_hash", "00000000000000000000"),
            ("peer_id", peer_id.as_str()),
            ("port", "6881"),
            ("uploaded", "0"),
            ("downloaded", "0"),
            ("left", "0"),
            ("compact", "1"),
            ("event", "started"),
        ])
        .send()
    {
        Ok(r) => r,
        Err(_) => return Status::Dead,
    };

    let status_code = response.status();
    let body = response.bytes().unwrap_or_default().to_vec();

    if is_parked_or_expired_domain(&body) {
        return Status::Invalid;
    }

    if bdecode_simple(&body) {
        return Status::Alive;
    }

    if status_code == StatusCode::OK {
        let head = String::from_utf8_lossy(&body[..body.len().min(200)]).to_lowercase();
        if head.contains("<html") || head.contains("<body") || head.contains("<head") {
            return Status::Invalid;
        }
        if body.len() > 50_000 {
            return Status::Invalid;
        }
        return Status::Dead;
    }

    if status_code == StatusCode::BAD_REQUEST || status_code == StatusCode::FORBIDDEN {
        if bdecode_simple(&body) {
            return Status::Alive;
        }
    }

    Status::Dead
}

fn check_udp_tracker(url: &str, timeout: Duration) -> Status {
    let parsed = match Url::parse(url) {
        Ok(p) => p,
        Err(_) => return Status::Invalid,
    };

    let host = match parsed.host_str() {
        Some(h) => h,
        None => return Status::Invalid,
    };
    let port = parsed.port().unwrap_or(80);

    let addr = match format!("{host}:{port}")
        .to_socket_addrs()
        .ok()
        .and_then(|mut it| it.next())
    {
        Some(a) => a,
        None => return Status::Invalid,
    };

    let socket = match UdpSocket::bind("0.0.0.0:0") {
        Ok(s) => s,
        Err(_) => return Status::Dead,
    };

    let _ = socket.set_read_timeout(Some(timeout));
    let _ = socket.set_write_timeout(Some(timeout));

    let connection_id: u64 = 0x41727101980;
    let action: u32 = 0;
    let transaction_id: u32 = rand::thread_rng().r#gen();

    let mut req = Vec::with_capacity(16);
    req.extend_from_slice(&connection_id.to_be_bytes());
    req.extend_from_slice(&action.to_be_bytes());
    req.extend_from_slice(&transaction_id.to_be_bytes());

    if socket.send_to(&req, addr).is_err() {
        return Status::Dead;
    }

    let mut response = [0u8; 16];
    match socket.recv_from(&mut response) {
        Ok((size, _)) if size >= 8 => Status::Alive,
        Ok(_) => Status::Alive,
        Err(_) => Status::Dead,
    }
}

fn check_wss_tracker(url: &str, timeout: Duration) -> Status {
    let parsed = match Url::parse(url) {
        Ok(p) => p,
        Err(_) => return Status::Invalid,
    };

    let host = match parsed.host_str() {
        Some(h) => h,
        None => return Status::Invalid,
    };
    let port = parsed.port().unwrap_or(443);

    let addr = match format!("{host}:{port}")
        .to_socket_addrs()
        .ok()
        .and_then(|mut it| it.next())
    {
        Some(a) => a,
        None => return Status::Invalid,
    };

    match TcpStream::connect_timeout(&addr, timeout) {
        Ok(_) => Status::Alive,
        Err(_) => Status::Dead,
    }
}

fn validate_tracker(tracker: &str, timeout: Duration) -> CheckResult {
    let parsed = match Url::parse(tracker) {
        Ok(p) => p,
        Err(_) => {
            return CheckResult {
                url: tracker.to_string(),
                status: Status::Invalid,
            }
        }
    };

    let protocol = parsed.scheme().to_lowercase();
    let status = match protocol.as_str() {
        "http" | "https" => check_http_tracker(tracker, timeout),
        "udp" => check_udp_tracker(tracker, timeout),
        "wss" => check_wss_tracker(tracker, timeout),
        _ => Status::Invalid,
    };

    CheckResult {
        url: tracker.to_string(),
        status,
    }
}

fn dedupe_keep_order(items: Vec<String>) -> Vec<String> {
    let mut seen = HashSet::new();
    let mut out = Vec::new();
    for item in items {
        if seen.insert(item.clone()) {
            out.push(item);
        }
    }
    out
}

fn sort_and_dedupe(mut items: Vec<String>) -> Vec<String> {
    items.sort_unstable();
    items.dedup();
    items
}

fn load_trackers(input_file: &str) -> Result<Vec<String>, String> {
    let text = fs::read_to_string(input_file)
        .map_err(|_| format!("Error: {input_file} not found or unreadable"))?;

    let normalized_text = text.replace(',', "\n");

    let mut trackers = Vec::new();
    for line in normalized_text.lines() {
        let parsed = parse_tracker_line(line);
        if parsed.is_empty() || parsed.starts_with('#') {
            continue;
        }

        for matched in tracker_regex().find_iter(&parsed) {
            if let Some(normalized) = normalize_tracker_url(matched.as_str()) {
                trackers.push(normalized);
            }
        }
    }

    Ok(dedupe_keep_order(trackers))
}

fn filter_blacklist(trackers: Vec<String>, blacklist_file: &str) -> Vec<String> {
    if !Path::new(blacklist_file).exists() {
        return trackers;
    }

    let text = match fs::read_to_string(blacklist_file) {
        Ok(t) => t,
        Err(_) => return trackers,
    };

    let patterns = text
        .lines()
        .map(|line| line.trim())
        .filter(|line| !line.is_empty() && !line.starts_with('#'))
        .map(|line| line.to_string())
        .collect::<Vec<_>>();

    if patterns.is_empty() {
        return trackers;
    }

    trackers
        .into_iter()
        .filter(|tracker| !patterns.iter().any(|p| tracker.contains(p)))
        .collect()
}

fn write_lines(path: PathBuf, lines: &[String]) -> Result<(), String> {
    let mut file = fs::File::create(&path)
        .map_err(|e| format!("Failed to create {}: {}", path.display(), e))?;
    for line in lines {
        file.write_all(line.as_bytes())
            .and_then(|_| file.write_all(b"\n"))
            .map_err(|e| format!("Failed to write {}: {}", path.display(), e))?;
    }
    Ok(())
}

fn write_best_file(alive_items: &[CheckResult], output_file: &str) -> Result<(), String> {
    let all_alive = sort_and_dedupe(alive_items.iter().map(|i| i.url.clone()).collect());
    write_lines(PathBuf::from(output_file), &all_alive)
}

fn split_best_file_by_protocol(output_file: &str) -> Result<(), String> {
    let output_path = PathBuf::from(output_file);
    let output_dir = output_path
        .parent()
        .map(|p| p.to_path_buf())
        .unwrap_or_else(|| PathBuf::from("."));

    let content = fs::read_to_string(&output_path)
        .map_err(|e| format!("Failed to read {}: {}", output_path.display(), e))?;

    let mut http = Vec::new();
    let mut https = Vec::new();
    let mut udp = Vec::new();
    let mut wss = Vec::new();

    for line in content.lines() {
        let tracker = line.trim();
        if tracker.is_empty() {
            continue;
        }

        if let Ok(parsed) = Url::parse(tracker) {
            match parsed.scheme() {
                "http" => http.push(tracker.to_string()),
                "https" => https.push(tracker.to_string()),
                "udp" => udp.push(tracker.to_string()),
                "wss" => wss.push(tracker.to_string()),
                _ => {}
            }
        }
    }

    let http = sort_and_dedupe(http);
    let https = sort_and_dedupe(https);
    let udp = sort_and_dedupe(udp);
    let wss = sort_and_dedupe(wss);

    write_lines(output_dir.join("trackers_best_http.txt"), &http)?;
    write_lines(output_dir.join("trackers_best_https.txt"), &https)?;
    write_lines(output_dir.join("trackers_best_udp.txt"), &udp)?;
    write_lines(output_dir.join("trackers_best_wss.txt"), &wss)?;

    Ok(())
}

fn html_escape(input: &str) -> String {
    input
        .replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
        .replace('"', "&quot;")
        .replace('\'', "&#39;")
}

fn tracker_protocol(url: &str) -> &'static str {
    if let Ok(parsed) = Url::parse(url) {
        match parsed.scheme() {
            "http" => "http",
            "https" => "https",
            "udp" => "udp",
            "wss" => "wss",
            _ => "other",
        }
    } else {
        "other"
    }
}

